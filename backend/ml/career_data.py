import json
import google.generativeai as genai
from config import GEMINI_API_KEY

if GEMINI_API_KEY:
    genai.configure(api_key=GEMINI_API_KEY)

def get_career_data(career_name: str) -> dict:
    # ✅ Use static data for known careers (FREE, instant, no API quota used)
    if career_name in CAREER_INFO:
        return CAREER_INFO[career_name]

    # Only call Gemini for careers NOT in our static data
    if GEMINI_API_KEY:
        try:
            model = genai.GenerativeModel('gemini-2.5-flash')
            prompt = f"""
        You are an expert career counselor. Provide a comprehensive roadmap and details for the career: "{career_name}".
        Return ONLY a valid JSON object matching this exact structure:
        {{
            "demand": "High" (or Medium/Low),
            "demand_color": "var(--secondary)" (use var(--secondary) for High, var(--primary) for Normal/Medium, var(--border) for Low),
            "salary_range": "₹X Lakhs - ₹Y Lakhs" (estimate for India),
            "growth": "A short sentence about expected growth.",
            "top_companies": ["Company1", "Company2", "Company3", "Company4"],
            "description": "A dynamic role focused on [Core Focus].",
            "required_skills": ["Skill 1", "Skill 2", "Skill 3", "Skill 4"],
            "roadmap": [
                {{"skill": "Master Skill 1", "priority": "Essential", "status": "Not Started", "courses": [{{"name": "Intro to Skill 1", "url": "#"}}]}},
                {{"skill": "Master Skill 2", "priority": "Essential", "status": "Not Started", "courses": [{{"name": "Intro to Skill 2", "url": "#"}}]}},
                {{"skill": "Master Skill 3", "priority": "High", "status": "Not Started", "courses": [{{"name": "Intro to Skill 3", "url": "#"}}]}}
            ]
        }}
        Provide exactly 4 or 5 required_skills and corresponding roadmap items.
        IMPORTANT: The required_skills MUST be relevant to the "{career_name}" career. Do NOT include unrelated programming skills for non-tech careers.
        Ensure it is strictly valid JSON without any markdown formatting like ```json ... ```. Do not add any text before or after the JSON.
        """
            response = model.generate_content(prompt)
            text_content = response.text.strip()

            if text_content.startswith("```json"):
                text_content = text_content[7:]
            if text_content.startswith("```"):
                text_content = text_content[3:]
            if text_content.endswith("```"):
                text_content = text_content[:-3]
            text_content = text_content.strip()

            dynamic_info = json.loads(text_content)
            CAREER_INFO[career_name] = dynamic_info  # Cache for future requests
            return dynamic_info

        except Exception as e:
            print(f"[ERROR] Gemini API failed for '{career_name}': {e}")

    # Fallback: use the CORRECT career's static data (not Software Engineer!)
    if career_name in CAREER_INFO:
        print(f"[FALLBACK] Using static data for '{career_name}'")
        return CAREER_INFO[career_name]

    # Last resort: unknown career with no static data and no Gemini
    print(f"[WARN] No data found for '{career_name}', using Software Engineer as fallback.")
    return CAREER_INFO.get('Software Engineer')

